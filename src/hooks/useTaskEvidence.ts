import { useQuery, useMutation, useQueryClient } from "@tanstack/react-query";
import { supabase } from "@/integrations/supabase/client";
import { useAuth } from "@/contexts/AuthContext";

export interface TaskEvidence {
  id: string;
  task_id: string;
  uploaded_by: string;
  uploader_name?: string;
  file_url: string;
  evidence_type: string;
  comment: string | null;
  created_at: string;
}

export const useTaskEvidence = (taskId: string | null) => {
  return useQuery({
    queryKey: ["task-evidence", taskId],
    queryFn: async () => {
      if (!taskId) return [];

      const { data, error } = await supabase
        .from("task_evidence")
        .select("*")
        .eq("task_id", taskId)
        .order("created_at", { ascending: false });

      if (error) throw error;

      const uploaderIds = [...new Set(data?.map((e) => e.uploaded_by))] as string[];
      let nameMap = new Map<string, string>();
      if (uploaderIds.length > 0) {
        const { data: profiles } = await supabase
          .from("profiles")
          .select("id, full_name")
          .in("id", uploaderIds);
        nameMap = new Map(profiles?.map((p) => [p.id, p.full_name]));
      }

      return (data || []).map((e) => ({
        ...e,
        uploader_name: nameMap.get(e.uploaded_by) || "Unknown",
      })) as TaskEvidence[];
    },
    enabled: !!taskId,
  });
};

export const useUploadEvidence = () => {
  const queryClient = useQueryClient();
  const { user } = useAuth();

  return useMutation({
    mutationFn: async ({
      taskId,
      file,
      evidenceType,
      comment,
    }: {
      taskId: string;
      file: File;
      evidenceType: string;
      comment?: string;
    }) => {
      if (!user) throw new Error("Not authenticated");

      const { data: profile } = await supabase
        .from("profiles")
        .select("id")
        .eq("user_id", user.id)
        .maybeSingle();

      if (!profile) throw new Error("Profile not found");

      // Upload file to storage
      const ext = file.name.split(".").pop();
      const filePath = `${user.id}/${Date.now()}-${Math.random().toString(36).substring(7)}.${ext}`;

      const { error: uploadError } = await supabase.storage
        .from("evidence")
        .upload(filePath, file);

      if (uploadError) throw uploadError;

      const { data: urlData } = supabase.storage
        .from("evidence")
        .getPublicUrl(filePath);

      // Insert evidence record
      const { error } = await supabase.from("task_evidence").insert({
        task_id: taskId,
        uploaded_by: profile.id,
        file_url: urlData.publicUrl,
        evidence_type: evidenceType,
        comment: comment || null,
      });

      if (error) throw error;

      // Log activity
      await supabase.from("task_activity_logs").insert({
        task_id: taskId,
        action_type: "evidence_uploaded",
        performed_by: profile.id,
        new_value: { evidence_type: evidenceType, file_name: file.name },
      });
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["task-evidence"] });
      queryClient.invalidateQueries({ queryKey: ["task-activity-logs"] });
    },
  });
};
